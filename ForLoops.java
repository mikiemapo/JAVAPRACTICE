/*for (initialization; condition; update)
 * code execution
 */

class ForLoops {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("i is :" + i);
        }
        System.out.println("============ ======== ======");
        for (int d = 0; d < 10; d += 3) {

            System.out.println("d is :" + d);

        }
        System.out.println("============ ======== ======");
        for (int k = 0; k < 10; k += 2) {

            System.out.println("k is :" + k);
        }
    }
}
